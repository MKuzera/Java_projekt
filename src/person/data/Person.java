package person.data;

public class Person {
    public Person(String name, String surname, String email, int ID, String adres) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.ID = ID;
        this.adres = adres;
    }




    private String name;
    private String surname;
    private String email;
    private int ID;
    private String adres;



}
